import numpy as np
def detect_faces_with_filter(pil_img, mtcnn, min_area_ratio=0.001, min_confidence=0.90, verbose=True):
    img_np = np.array(pil_img)
    width, height = pil_img.size
    img_area = width * height
    boxes, probs = mtcnn.detect(pil_img)

    if boxes is None or len(boxes) == 0:
        if verbose:
            print("‚ö†Ô∏è No faces detected.")
        return [], [], []

    kept, low_conf, too_small = [], [], []

    for i, (box, conf) in enumerate(zip(boxes, probs)):
        if conf is None or conf < min_confidence:
            if verbose:
                print(f"‚ö´ Face {i} skipped (low confidence: {conf:.2f})")
            low_conf.append((box, conf))
            continue

        x1, y1, x2, y2 = map(int, box)
        area = (x2 - x1) * (y2 - y1)
        if area / img_area < min_area_ratio:
            if verbose:
                print(f"‚ö™ Face {i} skipped (too small: {area/img_area:.2%})")
            too_small.append((box, conf))
            continue

        if verbose:
            print(f"üî¥ Face {i} kept: conf={conf:.2f}, size={area/img_area:.2%}")
        kept.append((box, conf))

    if verbose:
        print(f"üß† Total valid faces: {len(kept)}")
    return kept, low_conf, too_small


