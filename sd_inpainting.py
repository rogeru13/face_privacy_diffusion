def is_shiny_patch(
    img: Image.Image,
    box: Tuple[int,int,int,int],
    highlight_th: int,
    pixel_ratio_th: float
) -> bool:
    x1,y1,x2,y2 = box
    patch = img.crop((x1,y1,x2,y2)).convert("L")
    arr   = np.array(patch)
    shiny_pixels = (arr > highlight_th).sum()
    return (shiny_pixels / arr.size) > pixel_ratio_th



